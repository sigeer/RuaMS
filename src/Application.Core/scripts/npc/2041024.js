var status;

function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1) {
        cm.dispose();
    } else {
        if (mode == 0 && type > 0) {
            cm.dispose();
            return;
        }
        if (mode == 1) {
            status++;
        } else {
            status--;
        }

        if (status == 0) {
            cm.sendOk("对于那些能够完成伟大壮举并拥有坚定决心的人来说，#b最终目的地#k就在门后。机房一次只接受#r一个队伍#k，所以当穿过门时，请确保你的队伍已经准备好。");
            cm.dispose();
        }
    }
}